package day05__primitives_concatanation;

public class CellPhone {
    public static void main(String[] args){
        String brand = "Iphone";
        String model = " iPhone 11";
        String color = "Gold";
        double price = 699.99;
        int   storage = 256;
        boolean hasCamera = true;

        System.out.println(brand);
        System.out.println(model);
        System.out.println(color);
        System.out.println(price);
        System.out.println(storage);
        System.out.println(hasCamera);

        System.out.println("Brand is \t\t" + brand);
        System.out.println("Model is \t\t"   + model);
        System.out.println("Color is \t\t"   + color);
        System.out.println("Price is \t\t"   + price);
        System.out.println("Storage is\t\t"  + storage + "GB" );
        System.out.println("HasCamera is\t"  + hasCamera);



        String city = "Baku";
        System.out.println("City is \t\t" + city);



    }
}
