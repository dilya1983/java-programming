package Day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[] args){
        System.out.println("\"iphone case\",\"iphone\",\n" +
                "\"xbox\",\"monitor\",\"webcam\"");
        System.out.println("\"iphone\"");
        System.out.println("\"xbox\"");
        System.out.println("   ^    ");
        System.out.println(" /   \\");
        System.out.println(" |   |");
        System.out.println(" _____");
        System.out.println("\"telephone\",keyboard\",imac\",\nplating\",camera\",earphone\"");

        System.out.println();
        System.out.println("         ^^^^^^^^^^^^     ");
        System.out.println("         ^^^^^^^^^^^^     ");
        System.out.println("        /           \\\\   ");
        System.out.println("       /  ****       \\\\  ");
        System.out.println("      /  |::::|       \\\\ ");
        System.out.println("     ||  |_|__|        ||   ");
        System.out.println("     ||          *---* ||   ");
        System.out.println("     ||__________|   |_||   ");
        System.out.println("     ||----------| :>|-||   ");
        System.out.println("     ||----------|   |-||   ");
        System.out.println("                 *===*       ");
        System.out.println("                   +===+       ");
        System.out.println("                    +===+      ");
        System.out.println("                      +==+     ");


    }
}
