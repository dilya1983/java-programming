package day06_arithmetic_operarors;

public class ArithmeticOperators {
    public static void main(String[] args){
        System.out.println(5 + 10);//15
        System.out.println(10 - 3);//7
        System.out.println(3 * 3);//9
        System.out.println(15 / 5);//3
        System.out.println(13 % 5);//5 + 5 + [3 is remainder]

        System.out.println(-2 * 2);//-4

        int chairs = 4 + 2;//6
        System.out.println(chairs);//6

        int spoons = 10 + 20 + 12 + 0;//42
        System.out.println(spoons);//42

        // there are 42 spoons in kitchen
        System.out.println("there are "+ spoons + " spoons in kitchen");

    }
}
